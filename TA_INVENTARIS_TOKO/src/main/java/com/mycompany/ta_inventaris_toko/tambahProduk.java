    private void btCARIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCARIActionPerformed
        
    String kataKunci = KOTAKPENCARIAN.getText(); // Kata kunci yang dimasukkan oleh pengguna
    String[] hasilPencarian = data.cariProduk(kataKunci);

    // Lakukan sesuatu dengan hasil pencarian, misalnya menampilkan pada GUI atau melakukan operasi lainnya
    DefaultTableModel model = (DefaultTableModel) TABEL.getModel();
    model.setRowCount(0); // Menghapus semua baris pada tabel

    for (String produk : hasilPencarian) {
        int index = Arrays.asList(data.getProduk()).indexOf(produk); // Temukan indeks produk dalam array produk dari Database
        int jumlahProduk = data.getJumlahProduk()[index];
        int hargaProduk = data.getHargaProduk()[index];

        Object[] row = { produk, jumlahProduk, hargaProduk };
        model.addRow(row);
    }
    
    
    }//GEN-LAST:event_btCARIActionPerformed
    
