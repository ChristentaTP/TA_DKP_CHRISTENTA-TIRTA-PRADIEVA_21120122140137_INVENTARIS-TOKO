    private void btTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTambahActionPerformed
        // TODO add your handling code here:
    String nama = KOTAKNAMAPRODUK.getText();
    String jumlahInput = KOTAKJUMLAH.getText();
    String hargaInput = KOTAKHARGA.getText();

    if (evt.getSource() == btTambah) {
        if (nama.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan masukkan nama produk dengan benar!!!");
            return;
        }
            // Check if the first letter is a hyphen (-)
        if (nama.charAt(0) == '-') {
            JOptionPane.showMessageDialog(null, "Nama produk tidak boleh dimulai dengan tanda strip (-)!!!");
            return;
        }
        int jumlah = 0;
        if (!jumlahInput.isEmpty() && jumlahInput.matches("\\d++")) {
            jumlah = Integer.parseInt(jumlahInput);
            if (jumlah <= 0) {
                JOptionPane.showMessageDialog(null, "Masukkan jumlah dengan benar (angka harus lebih dari 0)!!!");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masukkan jumlah dengan benar!!!");
            return;
        }

        int harga = 0;
        if (!hargaInput.isEmpty() && hargaInput.matches("\\d++")) {
            harga = Integer.parseInt(hargaInput);
            if (harga <= 0) {
                JOptionPane.showMessageDialog(null, "Masukkan Harga dengan benar (angka harus lebih dari 0)!!!");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masukkan Harga dengan benar!!!");
            return;
        }

        if (data.isProdukExists(nama)) {
            JOptionPane.showMessageDialog(null, "Produk yang sama telah ada dalam daftar");
            return;
        }

        data.addProduk(nama, jumlah, harga);
        updateTable();

        // Bersihkan nilai yang dimasukkan pengguna
        KOTAKNAMAPRODUK.setText("");
        KOTAKJUMLAH.setText("");
        KOTAKHARGA.setText("");
    }
        
    }//GEN-LAST:event_btTambahActionPerformed

    
private void updateTable() {
    DefaultTableModel model = (DefaultTableModel) TABEL.getModel();
    model.setRowCount(0); // Menghapus semua baris pada tabel

    String[] produkList = data.getProduk();
    Integer[] jumlahProdukList = data.getJumlahProduk();
    Integer[] hargaProdukList = data.getHargaProduk();

    for (int i = 0; i < produkList.length; i++) {
        String produk = produkList[i];
        int jumlahProduk = jumlahProdukList[i];
        int hargaProduk = hargaProdukList[i];

        Object[] row = { produk, jumlahProduk, hargaProduk };
        model.addRow(row);
    }
}

        
        
